const OBVIOUS_LIFESTYLE_TITLE =
  /特殊性癖|婚前交渉|年上女性に甘え|K-POPアイドル.*細さ|痩せ薬|整形.*告白|本音激白.*恋愛|恋愛のリアル|モテ(る|ない)\?|東大.*モテ|脱糞|透けパン/;

/**
 * 性風俗・アダルト業界を扱ったタブロイド企画。
 * 2026-07-22実測で発見: 「風営法改正」等の実在する法律名を絡めることで見出し上は
 * 公共的な争点に見えるが、実態はAV女優・ホスト・風俗の個人体験を消費的に扱う企画で、
 * Geminiのtrack判定（「実在の法律名があれば公共的」という浅い理解）をすり抜けた。
 * 法律名の有無ではなく、性風俗業界の当事者ネタである点そのものを機械的に弾く。
 */
const ADULT_INDUSTRY_TABLOID_TITLE =
  /AV女優|風俗|ホスト.*(現実|裏側|闇)|地下アイドル|性で稼ぐ|男女ビジネス|裏引き|枕営業/;

export function isObviousNonDebateVideoTitle(title: string): boolean {
  return OBVIOUS_LIFESTYLE_TITLE.test(title) || ADULT_INDUSTRY_TABLOID_TITLE.test(title);
}
